function somar(x, y){
    return x+y;
}
function multiplicar(x, y){
    return x*y;
}
function dividir(x, y){
    return x/y;
}
function subtrair(x, y){
    return x-y;
}

somar(2,3);
multiplicar(3,5);
dividir(4,2);
subtrair(4,5);


function nomeCompleto(nome, sobrenome){
    return `${nome} ${sobrenome}`;
}
nomeCompleto('Vinicius', 'Peretta');